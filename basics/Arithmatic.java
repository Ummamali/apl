public class Arithmatic {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        System.out.printf("a + b = %d\n", a + b);
        System.out.printf("a - b = %d\n", a - b);
        System.out.printf("a * b = %d\n", a * b);
        System.out.printf("a / b = %d\n", a / b);
        System.out.printf("a %% b = %d\n", a % b);

    }
}
